package com.workforce;

public class Employee {

    private String EmpName;
    private int EmpSsn;
    private int EmpAge;
    private int EmpMemberYearSince;
    private int EmpPassword;
    private String EmpLastName;

    public String getEmpName() {
        return EmpName;
    }

    public void setEmpName(String empName) {
        EmpName = empName;
    }

    public int getEmpSsn() {
        return EmpSsn;
    }

    public void setEmpSsn(int empSsn) {
        EmpSsn = empSsn;
    }

    public int getEmpAge() {
        return EmpAge;
    }

    public void setEmpAge(int empAge) {
        EmpAge = empAge;
    }

    public int getEmpMemberYearSince() {
        return EmpMemberYearSince;
    }

    public void setEmpMemberYearSince(int empMemberYearSince) {
        EmpMemberYearSince = empMemberYearSince;
    }

    public int getEmpPassword() {
        return EmpPassword;
    }

    public void setEmpPassword(int empPassword) {
        EmpPassword = empPassword;
    }

    public String getEmpLastName() {
        return EmpLastName;
    }

    public void setEmpLastName(String empLastName) {
        EmpLastName = empLastName;
    }
}
